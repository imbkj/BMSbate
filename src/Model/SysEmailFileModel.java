package Model;

public class SysEmailFileModel {
	private int file_id;
	private int file_pfil_id;
	private String file_url;
	private String file_title;
	private String file_addname;
	private String file_addtime;
	public int getFile_id() {
		return file_id;
	}
	public void setFile_id(int file_id) {
		this.file_id = file_id;
	}
	public int getFile_pfil_id() {
		return file_pfil_id;
	}
	public void setFile_pfil_id(int file_pfil_id) {
		this.file_pfil_id = file_pfil_id;
	}
	public String getFile_url() {
		return file_url;
	}
	public void setFile_url(String file_url) {
		this.file_url = file_url;
	}
	public String getFile_title() {
		return file_title;
	}
	public void setFile_title(String file_title) {
		this.file_title = file_title;
	}
	public String getFile_addname() {
		return file_addname;
	}
	public void setFile_addname(String file_addname) {
		this.file_addname = file_addname;
	}
	public String getFile_addtime() {
		return file_addtime;
	}
	public void setFile_addtime(String file_addtime) {
		this.file_addtime = file_addtime;
	}
	
	
}
