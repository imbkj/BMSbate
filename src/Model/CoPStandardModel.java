package Model;

public class CoPStandardModel {

	private int cpst_id;
	private String cpst_name;
	private Integer cpst_type;

	public int getCpst_id() {
		return cpst_id;
	}

	public void setCpst_id(int cpst_id) {
		this.cpst_id = cpst_id;
	}

	public String getCpst_name() {
		return cpst_name;
	}

	public void setCpst_name(String cpst_name) {
		this.cpst_name = cpst_name;
	}

	public Integer getCpst_type() {
		return cpst_type;
	}

	public void setCpst_type(Integer cpst_type) {
		this.cpst_type = cpst_type;
	}
	
}
