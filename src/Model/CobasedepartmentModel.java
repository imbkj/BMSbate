package Model;

public class CobasedepartmentModel {
	private int coba_pid;
	private String coba_pname;
	private int coba_ppid;
	private int cid;
	
	public CobasedepartmentModel() {
		super();
	}
	public int getCoba_pid() {
		return coba_pid;
	}
	public void setCoba_pid(int coba_pid) {
		this.coba_pid = coba_pid;
	}
	public String getCoba_pname() {
		return coba_pname;
	}
	public void setCoba_pname(String coba_pname) {
		this.coba_pname = coba_pname;
	}
	public int getCoba_ppid() {
		return coba_ppid;
	}
	public void setCoba_ppid(int coba_ppid) {
		this.coba_ppid = coba_ppid;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	
	
}
