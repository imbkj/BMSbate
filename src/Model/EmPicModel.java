package Model;

public class EmPicModel {
	private String empic_type;
	private String empic_name;
	private String empic_time;
	private String empic_url;

	public String getEmpic_type() {
		return empic_type;
	}

	public void setEmpic_type(String empic_type) {
		this.empic_type = empic_type;
	}

	public String getEmpic_name() {
		return empic_name;
	}

	public void setEmpic_name(String empic_name) {
		this.empic_name = empic_name;
	}

	public String getEmpic_time() {
		return empic_time;
	}

	public void setEmpic_time(String empic_time) {
		this.empic_time = empic_time;
	}

	public String getEmpic_url() {
		return empic_url;
	}

	public void setEmpic_url(String empic_url) {
		this.empic_url = empic_url;
	}

}
