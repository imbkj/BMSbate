package Model;

public class PubRegionModel {
	private int id; // 地区表id
	private String name; // 地区名称

	public PubRegionModel(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
